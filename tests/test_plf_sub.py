from rtcvis import *


def test_1():
    a = PLF([(0, 0), (2, 2)])
    b = PLF([(0, 0), (1, 0.5), (2, 2.5)])
    assert a - b == PLF([(0, 0), (1, 0.5), (2, -0.5)])


def test_2():
    a = PLF([(0, 0), (1, 1), (1, 0), (2, 1)])
    b = PLF([(0, 1), (2, 3)])
    assert a - b == PLF([(0, -1), (1, -1), (1, -2), (2, -2)])


def test_3():
    a = PLF([(0, 1), (4, 5), (8, 4)])
    b = PLF([(0, 2), (2, 0), (5, 1), (8, -2)])
    assert a - b == PLF([(0, -1), (2, 3), Point(4, 5 - 2 / 3), (5, 3.75), (8, 6)])


def test_4():
    a = PLF([(0, 2), (2, 0), (5, 1), (8, -2)])
    assert a - a == PLF([(0, 0), (2, 0), (5, 0), (8, 0)])


def test_5():
    a = PLF([(-1, 0), (1, 2)])
    b = PLF([(-1, 0), (0, 0), (0, 1), (1, 1)])
    assert a - b == PLF([(-1, 0), (0, 1), (0, 0), (1, 1)])
